# Go Fish !!

go_fish_rules = """
THE PACK: 

    The standard 52-card pack is used. Some cards will be dealt and the rest will form the stock pile.



OBJECT OF THE GAME: 

    The goal is to win the most "books" of cards. A book is any pair of a kind, such as two Kings, two Aces, two 2s, 
    and so on.



RANK OF CARDS: 

    The cards rank from Ace (high) to 2 (low). The suits are not important, only the card numbers are relevant, such as 
    two 3s, two 10s, and so on.



THE DEAL:

    Any player deals one card face up to each player. The player with the lowest card is the dealer. The dealer shuffles
    the cards, and the player to the right cuts them. (And computer always the dealer.)
    
    The dealer completes the cut and deals the cards clockwise one at a time, face down, beginning with the player to 
    the left. If two people are playing, each player receives seven cards. If more than two people are playing, each 
    receives five cards. The remainder of the pack is placed face down on the table to form the stock. (In this game 
    program limited player as two people playing, playing with computer in 1 player mode or a friend in 2 player mode.)


THE PLAY:
    The player to the left of the dealer looks directly at any opponent and says, for example, "Give me your kings," 
    usually addressing the opponent by name and specifying the rank that they want, from ace down to two. The player 
    who is "fishing" must have at least one card of the rank that was asked for in their hand. The player who is 
    addressed must hand over all the cards requested. If the player has none, they say, "Go fish!" and the player who 
    made the request draws the top card of the stock and places it in their hand. 
    
    If a player gets one or more cards of the named rank that was asked for, they are entitled to ask the same or 
    another player for a card. The player can ask for the same card or a different one. So long as the player succeeds 
    in getting cards (makes a catch), their turn continues. When a player makes a catch, they must reveal the card so 
    that the catch is verified. If a player gets the pair card of a book, the player shows all pair cards, places them 
    on the table face up in front of everyone, and plays again.
    
    If the player goes fishing without "making a catch" (does not receive a card he asked for), the turn passes to the 
    left. 
    
    The game ends when all thirteen books have been won. The winner is the player with the most books. During the game, 
    if a player is left without cards, they may (when it's their turn to play), draw from the stock and then ask for 
    cards of that rank. If there are no cards left in the stock, they are out of the game.

"""

